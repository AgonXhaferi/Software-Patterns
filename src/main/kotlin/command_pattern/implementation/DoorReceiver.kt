package main.kotlin.command_pattern.implementation

class DoorReceiver {
    fun openHorizontally() {
        println("Open door horizontally.")
    }

    fun closeHorizontally() {
        println("Close door horizontally.")
    }

    fun openVertically() {
        println("Open door vertically.")
    }

    fun closeVertically() {
        println("Close door vertically.")
    }
}